package inheritance;

public class Assignment_strings {

	public static void main(String[] args) 
	{
		
			
			{
				String s1= new String("Manish kumar tiwari");
				System.out.println(s1);
				System.out.println(s1.length());
				System.out.println(s1.charAt(0));
				System.out.println(s1.indexOf('i'));
				System.out.println(s1.toLowerCase());
				System.out.println(s1.toUpperCase());
				System.out.println(s1.substring(4));//from fourt char
				System.out.println(s1.substring(0, 6));//(4-3 index)
				System.out.println(s1.substring(7, 12));
				System.out.println(s1.substring(13));
				
				
			}
		}
}

	
