package basicprog;

public class Operator1 {

	public static void main(String[] args) 
	{
	  
		int a=20;
		int b=10;
		
		
		int sum=a+b;
		System.out.println(sum);
		
		int sub=a-b;
		System.out.println(sub);
		
		int mul=a*b;
		System.out.println(mul);
		
		int div=a/b;
		System.out.println(div);
		//quotient
		
		int modulus=a%b;
		System.out.println(modulus);
		//remainder
		
		
		
		
		
		
		

	}

}
